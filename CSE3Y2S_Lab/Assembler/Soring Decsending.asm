.MODEL SMALL
.STACK 100H    

.DATA
	str DB 90 DUP ?
	MSG DB "ENTER A LETTER STRING WITH SAME CASE: $"
	RES DB "Decsending Order: $"
	 
.CODE   
    MAIN PROC
    MOV AX, @DATA
	MOV DS, AX
	
	MOV AH,9
	LEA DX, MSG
	INT 21H
	
	MOV AH,1
	
NEXT:
	INT 21H
	CMP AL,0DH
	JZ EXIT
	
	MOV str[SI], AL
	
	INC SI
    JMP NEXT
	
	EXIT:
	MOV CX, SI
	
LOP1:
	DEC SI
	MOV DI,SI
	
LOP2:
    DEC DI
	MOV BL, str[SI]
	CMP BL, str[DI]
	JL SKIP
	XCHG BL, str[DI]
	MOV str[SI],BL
	
SKIP:
    CMP DI,0
	JNE LOP2
	
S2:	
    CMP SI,1
	JG LOP1

	MOV AH, 2
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H
	
	MOV AH,9
	LEA DX, RES
	INT 21H

	MOV DI, 0
	MOV AH,2
NEXT2:
	MOV DL, str[DI]
	INT 21H
	INC DI
	CMP DI,CX
	JB NEXT2
	
	MOV AH,4CH
	INT 21H
	MAIN ENDP
	END MAIN
    